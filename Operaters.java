public class Operaters {
    public static void main(Pattern ar[]){
        /*logical operaters*/
        int a=25;
        int b=75;
        System.out.println("AND && :"+(a>=35 && b>=35));
        System.out.println("OR || :"+(a>=35 || b>=35));
        /*conditional operaters or ternary operaters*/
        int c=a>b?a:b;
        System.out.println(c);
        System.out.println(a==75);
        System.out.println(c==75);
    }
}
