import java.util.Scanner;
public class New12 {
    public static void main(String ar[]){
        Scanner sc=new Scanner(System.in);
        String str=sc.next();
        int a=str.length();
        char arr[]=new char[a];
        for(int i=a;i<a;i--){
            arr[i]=str.charAt(i);
        }
        System.out.println(arr);
    }
}
